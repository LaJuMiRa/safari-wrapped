export async function getLang() {
  try {
    if (typeof chrome !== 'undefined' && chrome.storage) {
      const s = await chrome.storage.local.get('lang');
      if (s.lang === 'en' || s.lang === 'de') return s.lang;
    }
  } catch { /* noop */ }
  return 'de';
}

export function makeT(lang) {
  const dict = STRINGS[lang] || STRINGS.de;
  return (key, vars) => {
    let str = dict[key] ?? STRINGS.de[key] ?? key;
    if (vars) for (const k in vars) str = str.split(`{${k}}`).join(String(vars[k]));
    return str;
  };
}
